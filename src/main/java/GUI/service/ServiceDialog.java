    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
      if (!validateForm()) return;
        String name = txtName.getText().trim();
        String unit = txtUnit.getText().trim();
        double price = parsePrice(txtPrice.getText());
        String type = (String) cbChargeType.getSelectedItem();
        boolean active = chkActive.isSelected();

        if (editMode && currentService != null) {
            currentService.setName(name);
            currentService.setUnit(unit);
            currentService.setUnitPrice(price);
            currentService.setChargeType(type);
            currentService.setActive(active);
            resultService = currentService;
        } else {
            resultService = new ServiceDTO(0, name, unit, price, type, active);
        }
        dispose();
    }//GEN-LAST:event_btnSaveActionPerformed

