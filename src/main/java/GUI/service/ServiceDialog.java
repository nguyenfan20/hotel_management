    private void setupComboBox() {
        cbChargeType.removeAllItems();
        cbChargeType.addItem("FREE");
        cbChargeType.addItem("PER_UNIT");
        chkActive.setSelected(true);
    }

    public ServiceDTO openForCreate() {
        editMode = false;
        currentService = null;
        lblTitle.setText("Thêm Dịch vụ");
        clearForm();
        setVisible(true);
        return resultService;
    }

    public ServiceDTO openForEdit(ServiceDTO s) {
        editMode = true;
        currentService = s;
        lblTitle.setText("Sửa Dịch vụ");
        fillForm(s);
        setVisible(true);
        return resultService;
    }

    private void clearForm() {
        txtName.setText("");
        txtUnit.setText("");
        txtPrice.setText("");
        cbChargeType.setSelectedItem("PER_UNIT");
        chkActive.setSelected(true);
    }

    private void fillForm(ServiceDTO s) {
        txtName.setText(s.getName());
        txtUnit.setText(s.getUnit());
        txtPrice.setText(formatMoney(s.getUnitPrice()));
        cbChargeType.setSelectedItem(s.getChargeType());
        chkActive.setSelected(s.isActive());
    }

    private String formatMoney(double v) {
        BigDecimal bd = BigDecimal.valueOf(v).stripTrailingZeros();
        return bd.toPlainString();
    }

    private Double parsePrice(String text) {
        try {
            String cleaned = text.trim().replace(",", "");
            return Double.parseDouble(cleaned);
        } catch (Exception e) {
            return null;
        }
    }

    private boolean validateForm() {
        String name = txtName.getText().trim();
        String unit = txtUnit.getText().trim();
        String priceText = txtPrice.getText().trim();
        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên dịch vụ!", "Thiếu thông tin", JOptionPane.WARNING_MESSAGE);
            txtName.requestFocus();
            return false;
        }

        if (unit.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đơn vị tính!", "Thiếu thông tin", JOptionPane.WARNING_MESSAGE);
            txtUnit.requestFocus();
            return false;
        }

        Double price = parsePrice(priceText);
        if (price == null || price < 0) {
            JOptionPane.showMessageDialog(this, "Giá phải là số hợp lệ và >= 0!", "Lỗi nhập giá", JOptionPane.ERROR_MESSAGE);
            txtPrice.requestFocus();
            return false;
        }

        if (cbChargeType.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn loại tính phí.", "Thiếu thông tin", JOptionPane.WARNING_MESSAGE);
            return false;
        }

        return true;
    }
